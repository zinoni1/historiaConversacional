public class Objecte {
    public String nom;
    public String descripcio;

    public Objecte(String nom, String descripcio) {
        this.nom = nom;
        this.descripcio = descripcio;
    }

    public String getNom() {
        return nom;
    }

    public String getDescripcio() {
        return this.descripcio;
    }

    // crear objecte donut

}
